-----------SQL GRUPO DE COMANDOS--------------

DML
	data manipulation language
		insert - insere registros
		update - atualiza registros
		delete - exclui registros
DDL
	data definition language
		create - cria nova tabela
		alter - altera objeto ja existente
		drop - exclui uma tabela
DCL
	data control language 
		grant - da privilegios a um usuario
		reboke - retira privilegios de um usuario
DQL
	sata query language
		select - seleciona registros


-----------SQL TIPOS DE DADOS--------------

char - string de tamanho fixo, maximo 8000 caracteres
varchar - string de tamanho variavel, maximo 8000 caracteres
nchar - unicode tamanho fixo, maximo 4000 caracteres
nvarchar - unicode tamanho variavel, maximo 4000 caracteres
bit - 0, 1 ou nulo
tinyint - numeros inteiros 0 a 255
smallint - numeros inteiros -32768 a 32767
int - numeros inteiros -2,147,483,648 a 2,147,483,647
bigint - numeros inteiros -9,223,372,036,854,775,808 a 9,223,372,036,854,775,807
real - numero de ponto flutuante
datetime - datas de 01/01/1753 a 31/12/9999
smalldatetime - datas de 01/01/1900 a 06/06/2079
date - datas de 01/01/0001 a 31/12/9999
time - horas
text - cadeia de caracteres de tamanho variavel
money - dados monetarios

