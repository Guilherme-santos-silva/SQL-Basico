/**
-----------SQL CONSTRAINS RESTRIÇÕES--------------

not null - impõe a uma coluna não aceitar valores nulos NULL
unique - define de forma unica cada registro (funcina como chave primaria mas com o diferencial que podemos ter mais de um campo unique em uma tabela como campo de cpf e rg)
primary key - chave primaria define de forma unica cada registro nao podendo ter valores NULL, cada tabela tem apenas uma chave primaria mesmo se composto por varias colunas
foreign key - chave estrangeira um campo em uma tabela que aponta para uma chave primaria em outra tabela
check - limita uma faixa de valores em uma coluna
default - usada para inserir um valor padrão se nao for inserido nenhum valor na coluna

**/